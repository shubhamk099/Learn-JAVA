
import java.lang.System;

public class Variables{
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int marksOfStudent = 94;
        System.out.println(num1+num2);
        System.out.println(marksOfStudent);
    }
}

// Variables names are case sensative
// Variables must start with either a letter, _ , $
// Variables name cannot start with a number
// Whitespaces are not allowed in variables names
// Variables names cannot be a keyword
// Follow camelCase notation